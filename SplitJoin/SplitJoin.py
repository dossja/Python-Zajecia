#Lista rozdzielona przecinkami
cechy = "niski, zabawny, usmiechniety, inteligentny"
print(cechy)
print("\n******************\n")

#Ustawianie znacznika podziału na ", "
splited = cechy.split(", ")
print(splited)
print("\n******************\n")

#Łączenie listy przy pomocy wybranego znacznika
joined = " i ".join(splited)
print(joined)
print("\n******************\n")
